def polish_text(text: str) -> str:
    return GLOBAL.get(text, text)


def main() -> None:
    shutil.copyfile(SRC, OUT)
    with zipfile.ZipFile(OUT, "r") as zin:
        infos = zin.infolist()
        data = {info.filename: zin.read(info.filename) for info in infos}

    for slide_no in range(1, 15):
        name = f"ppt/slides/slide{slide_no}.xml"
        if name not in data:
            continue
        root = ET.fromstring(data[name])
        nodes = root.findall(f".//{{{NS_A}}}t")
        overrides = SLIDE_OVERRIDES.get(slide_no, {})
        for idx, node in enumerate(nodes):
            if idx in overrides:
                node.text = overrides[idx]
            elif node.text:
                node.text = polish_text(node.text)
        data[name] = ET.tostring(root, encoding="utf-8", xml_declaration=True)

    with zipfile.ZipFile(OUT, "w", zipfile.ZIP_DEFLATED) as zout:
        for info in infos:
            zi = zipfile.ZipInfo(info.filename, info.date_time)
            zi.compress_type = zipfile.ZIP_DEFLATED
            zi.comment = info.comment
            zi.extra = info.extra
            zi.internal_attr = info.internal_attr
            zi.external_attr = info.external_attr
            zi.create_system = info.create_system
            zout.writestr(zi, data[info.filename])

    print(OUT.resolve())


if __name__ == "__main__":
    main()
